package com.fgd.springtx.entiries;


public class User {

	/**
	* null
	*/
	private long id;
	/**
	* null
	*/
	private String username;
	/**
	* null
	*/
	private long age;
	/**
	* null
	*/
	private String info;
	/**
	* null
	*/
	private long addId;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getUsername() {
    return username;
  }

  public void setUsername(String username) {
    this.username = username;
  }


  public long getAge() {
    return age;
  }

  public void setAge(long age) {
    this.age = age;
  }


  public String getInfo() {
    return info;
  }

  public void setInfo(String info) {
    this.info = info;
  }


  public long getAddId() {
    return addId;
  }

  public void setAddId(long addId) {
    this.addId = addId;
  }

}
